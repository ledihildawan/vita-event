import DefaultLayout from '../../layout/DefaultLayout';
import CardDataStats from '../../components/CardDataStats';

import React, { useState } from 'react';

import { toast } from 'react-toastify';
import { useQuery } from 'react-query';
import { useNavigate } from 'react-router-dom';

import api from '../../api';

import useAuth from '../../hooks/useAuth';

const Dashboard: React.FC = () => {
  const [u]: any = useAuth();
  const [data, setData] = useState<any>();

  const navigate = useNavigate();

  useQuery({
    queryFn: () => api.get('dashboard'),
    onSuccess: (res: any) => {
      setData(res.data);
    },
    onError: () => {
      toast.error('Request Failed');
    },
  });

  const handleClick = (path: string) => {
    navigate(path)
  }

  return (
    <DefaultLayout>
      <div className="grid grid-cols-1 gap-4 md:grid-cols-2 md:gap-6 xl:grid-cols-4 2xl:gap-7.5">
        <CardDataStats
          title={`Total ${u?.role === 'Company' ? 'proposed' : ''} events`}
          total={u?.role === 'Company' ? data?.totalEventsTheUserHas || 0 : data?.totalEvents ?? 0}
          onClick={() => { handleClick('/events') }}
        >
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="stroke-primary dark:stroke-white size-6">
            <path strokeLinecap="round" strokeLinejoin="round" d="M9 12h3.75M9 15h3.75M9 18h3.75m3 .75H18a2.25 2.25 0 0 0 2.25-2.25V6.108c0-1.135-.845-2.098-1.976-2.192a48.424 48.424 0 0 0-1.123-.08m-5.801 0c-.065.21-.1.433-.1.664 0 .414.336.75.75.75h4.5a.75.75 0 0 0 .75-.75 2.25 2.25 0 0 0-.1-.664m-5.8 0A2.251 2.251 0 0 1 13.5 2.25H15c1.012 0 1.867.668 2.15 1.586m-5.8 0c-.376.023-.75.05-1.124.08C9.095 4.01 8.25 4.973 8.25 6.108V8.25m0 0H4.875c-.621 0-1.125.504-1.125 1.125v11.25c0 .621.504 1.125 1.125 1.125h9.75c.621 0 1.125-.504 1.125-1.125V9.375c0-.621-.504-1.125-1.125-1.125H8.25ZM6.75 12h.008v.008H6.75V12Zm0 3h.008v.008H6.75V15Zm0 3h.008v.008H6.75V18Z" />
          </svg>
        </CardDataStats>
        <CardDataStats
          title="Total pending events"
          total={data?.totalPendingEvents ?? 0}
          onClick={() => { handleClick('/events?status=Pending') }}
        >
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="stroke-primary dark:stroke-white size-6">
            <path strokeLinecap="round" strokeLinejoin="round" d="M12 6v6h4.5m4.5 0a9 9 0 1 1-18 0 9 9 0 0 1 18 0Z" />
          </svg>
        </CardDataStats>
        <CardDataStats
          title="Total approved events"
          total={data?.totalApprovedEvents ?? 0}
          onClick={() => { handleClick('/events?status=Approved') }}
        >
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="stroke-primary dark:stroke-white size-6">
            <path strokeLinecap="round" strokeLinejoin="round" d="M9 12.75 11.25 15 15 9.75M21 12c0 1.268-.63 2.39-1.593 3.068a3.745 3.745 0 0 1-1.043 3.296 3.745 3.745 0 0 1-3.296 1.043A3.745 3.745 0 0 1 12 21c-1.268 0-2.39-.63-3.068-1.593a3.746 3.746 0 0 1-3.296-1.043 3.745 3.745 0 0 1-1.043-3.296A3.745 3.745 0 0 1 3 12c0-1.268.63-2.39 1.593-3.068a3.745 3.745 0 0 1 1.043-3.296 3.746 3.746 0 0 1 3.296-1.043A3.746 3.746 0 0 1 12 3c1.268 0 2.39.63 3.068 1.593a3.746 3.746 0 0 1 3.296 1.043 3.746 3.746 0 0 1 1.043 3.296A3.745 3.745 0 0 1 21 12Z" />
          </svg>
        </CardDataStats>
        <CardDataStats
          title="Total rejected events"
          total={data?.totalRejectedEvents ?? 0}
          onClick={() => { handleClick('/events?status=Rejected') }}
        >
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="size-6 stroke-primary dark:stroke-white">
            <path strokeLinecap="round" strokeLinejoin="round" d="m9.75 9.75 4.5 4.5m0-4.5-4.5 4.5M21 12a9 9 0 1 1-18 0 9 9 0 0 1 18 0Z" />
          </svg>
        </CardDataStats>
      </div>
    </DefaultLayout>
  );
};

export default Dashboard;
